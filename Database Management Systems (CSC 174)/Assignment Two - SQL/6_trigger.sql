DELIMITER $
CREATE TRIGGER del_album
AFTER DELETE ON Music_Album 
FOR EACH ROW
BEGIN
	DELETE FROM Music_CD 
	WHERE OLD.Item_ID = Item_ID;
END;
$
DELIMITER;

DELIMITER $
CREATE TRIGGER del_CD
AFTER DELETE ON Music_CD 
FOR EACH ROW
BEGIN
	DELETE FROM Item 
	WHERE OLD.Item_ID = Item_ID;
END;
$
DELIMITER;
